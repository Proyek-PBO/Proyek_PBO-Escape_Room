    @Override
    public void Select(JLabel curr) {
        if (curr.getName().equals("gunting")) {
            gunting = true;
        }else if(curr.getName().equals("obeng")){
            obeng = true;
        }else if (curr.getName().equals("crowbar")) {
            crowbar = true;
        }else if (curr.getName().equals("palu")) {
            palu = true;
        }else if (curr.getName().equals("palinggis")) {
            palinggis = true;
        }
    }

