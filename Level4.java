    private boolean cek(javax.swing.JLabel item){
        for (Map.Entry<JLabel, Boolean> entry : have.entrySet()) {
            JLabel key = entry.getKey();
            Boolean value = entry.getValue();
            if (key == item) {
                return value;
            }
        }
        return false;
    }
    
