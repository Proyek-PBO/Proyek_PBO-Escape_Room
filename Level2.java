    @Override
    public void Select(javax.swing.JLabel curr) {
        if (curr.getName().equals("arloji")) {
            Arloji = true;
        } else if (curr.getName().equals("safekey")) {
            Safekey = true;
        } else if (curr.getName().equals("chestkey")) {
            Chestkey = true;
        } else if (curr.getName().equals("isichestkecil")) {
            Isichestkecil = true;
        }
    }

