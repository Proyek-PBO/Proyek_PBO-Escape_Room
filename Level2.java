    private void new_Set() {
        for (int j = 0; j < 4; j++) {
            if (i.get(j) != null) {
                if (i.get(j) == arloji) {
                    i_arloji.setLocation(1250, coord_y[j]);
                } else if (i.get(j) == isichestkecil) {
                    i_isichestkecil.setLocation(1250, coord_y[j]);
                } else if (i.get(j) == safekey) {
                    i_safekey.setLocation(1250, coord_y[j]);
                } else if (i.get(j) == chestkey) {
                    i_chestkey.setLocation(1250, coord_y[j]);
                }
            }
        }
    }

    @Override
    public void Hint() {
        if (chestkey.isVisible() && !hchestkey) {
            hintchestkey.setVisible(true);
            hchestkey = true;
        } else if (safekey.isVisible() && !hsafekey) {
            hintsafekey.setVisible(true);
            hsafekey = true;
        } else if (!chestkey.isVisible() && !isichestkecil.isVisible() && !hisichest && !chestclose.isVisible()) {
            hintisichest.setVisible(true);
            hisichest = true;
        }else if(!arloji.isVisible() && !safekey.isVisible() && !harloji && !safeclose.isVisible()){
            hintarloji.setVisible(true);
            harloji = true;
        }else{
            available_hint = false;
            JOptionPane.showMessageDialog(this, "There are no hints we can give, try to solve the existing puzzle", "Hint", JOptionPane.ERROR_MESSAGE);
        }
    }

    @Override
    public void normalTimer() {
        timer = new Timer(1000, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {

                second++;

                ddSecond = dFormat.format(second);
                ddMinute = dFormat.format(minute);
                Timer.setText(ddMinute + ":" + ddSecond);

                if (second == 60) {
                    second = 0;
                    minute++;

                    ddSecond = dFormat.format(second);
                    ddMinute = dFormat.format(minute);
                    Timer.setText(ddMinute + ":" + ddSecond);
                }
            }
        });
    }

    @Override
    public void Playmusic(String musiclocation) {
        try {
            File musicPath = new File(musiclocation);

            if (musicPath.exists()) {
                AudioInputStream audioInput = AudioSystem.getAudioInputStream(musicPath);
                clip = AudioSystem.getClip();
                clip.open(audioInput);
                clip.start();
                clip.loop(Clip.LOOP_CONTINUOUSLY);
            } else {
                System.out.println("Can't find file");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
